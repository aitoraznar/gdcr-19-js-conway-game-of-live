const chalk = require('chalk');
var clear = require("cli-clear");
const { createUniverse, updateStatus } = require("../src/universe");

function game(initialState) {
  let universe = createUniverse(initialState);
  print(universe);

  setInterval(() => {
    universe = updateStatus(universe);
    print(universe);
  }, 100);
}

function print(universe) {
  clear();
  universe.forEach(row => {
    console.log(row.map(column => {
      return column ? chalk.bgWhite('  ') : chalk.bgBlack('  ')
    }).join(''));
  });
}

